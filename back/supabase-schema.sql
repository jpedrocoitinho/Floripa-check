CREATE TABLE IF NOT EXISTS praias (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nome VARCHAR(120) NOT NULL UNIQUE,
  bairro VARCHAR(120) NOT NULL,
  descricao TEXT NOT NULL,
  imagem_url VARCHAR(600) NOT NULL,
  criado_em TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS avaliacoes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  praia_id BIGINT NOT NULL REFERENCES praias(id) ON DELETE CASCADE,
  usuario VARCHAR(120) NOT NULL,
  nota NUMERIC(2, 1) NOT NULL,
  comentario TEXT NOT NULL,
  condicoes JSONB NOT NULL DEFAULT '[]'::jsonb,
  criado_em TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS cadastros (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nome VARCHAR(120) NOT NULL,
  email VARCHAR(180) NOT NULL UNIQUE,
  senha_hash VARCHAR(255) NOT NULL,
  criado_em TIMESTAMPTZ DEFAULT NOW(),
  atualizado_em TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS logins (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cadastro_id BIGINT REFERENCES cadastros(id) ON DELETE SET NULL,
  email VARCHAR(180) NOT NULL,
  sucesso BOOLEAN NOT NULL DEFAULT FALSE,
  ip VARCHAR(80),
  user_agent VARCHAR(255),
  criado_em TIMESTAMPTZ DEFAULT NOW()
);

ALTER TABLE praias ENABLE ROW LEVEL SECURITY;
ALTER TABLE avaliacoes ENABLE ROW LEVEL SECURITY;
ALTER TABLE cadastros ENABLE ROW LEVEL SECURITY;
ALTER TABLE logins ENABLE ROW LEVEL SECURITY;
